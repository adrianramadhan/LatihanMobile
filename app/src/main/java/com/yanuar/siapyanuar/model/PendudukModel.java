package com.yanuar.siapyanuar.model;

public class PendudukModel {
    private String _nik;
    private String _nama;

    public String get_nik() {
        return _nik;
    }

    public void set_nik(String _nik) {
        this._nik = _nik;
    }

    public String get_nama() {
        return _nama;
    }

    public void set_nama(String _nama) {
        this._nama = _nama;
    }

    public String get_jk() {
        return _jk;
    }

    public void set_jk(String _jk) {
        this._jk = _jk;
    }

    public String get_kecamatan() {
        return _kecamatan;
    }

    public void set_kecamatan(String _kecamatan) {
        this._kecamatan = _kecamatan;
    }

    public String get_agama() {
        return _agama;
    }

    public void set_agama(String _agama) {
        this._agama = _agama;
    }

    private String _jk;
    private String _kecamatan;
    private String _agama;

}
